public final class q1 {
    public Comparable[] sSort(Comparable[] x){
        Comparable[] y = new Comparable[x.length];
        for(int i = 0; i < x.length; i++)
            y[i] = x[i];
        Comparable low;
        for(int i = 0; i < y.length; i++){
            for(int j = i; j < y.length; j++){
                low = y[j];
            }
        }
    }
}
